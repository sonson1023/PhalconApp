CREATE TABLE `word` (
  `idx` int(11) NOT NULL AUTO_INCREMENT,
  `word` varchar(45) COLLATE utf8_bin NOT NULL,
  `meaning` varchar(45) COLLATE utf8_bin DEFAULT NULL,
  `kausu` int(11) DEFAULT NULL COMMENT '0-m, 1-f, 2-n, 3-pl',
  PRIMARY KEY (`idx`,`word`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;
